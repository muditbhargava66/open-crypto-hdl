    reg  [3:0] phase;   // 0=idle, 1=running, 2=output
    reg        enc_reg;

    integer i;

    // ---- Precompute all 16 subkeys ----
    task gen_subkeys;
        reg [55:0] cd0;
        reg [27:0] ci, di;
        integer gi;
        reg [4:0] ra;
        begin
            cd0 = pc1(key);
            ci = cd0[55:28]; di = cd0[27:0];
            for (gi = 0; gi < 16; gi = gi + 1) begin
                ra = rot_amt(gi[3:0]);
                ci = (ra == 5'd1) ? {ci[26:0], ci[27]} : {ci[25:0], ci[27:26]};
                di = (ra == 5'd1) ? {di[26:0], di[27]} : {di[25:0], di[27:26]};
                subkeys[gi] = pc2({ci, di});
            end
        end
    endtask

    // Temporaries for FSM (module-level for Verilog 2001 compatibility)
    reg  [63:0] ip_out_tmp;
    reg   [3:0] sk_idx_tmp;
    reg  [31:0] f_out_tmp;
    reg  [31:0] new_R_tmp;

    always @(posedge clk or negedge rst_n) begin
        if (!rst_n) begin
            phase <= 4'd0;
            done  <= 1'b0;
        end else begin
            done <= 1'b0;

            case (phase)
                4'd0: begin
                    if (load) begin
                        gen_subkeys();
                        enc_reg    <= encrypt;
                        ip_out_tmp  = ip(block);
                        L          <= ip_out_tmp[63:32];
                        R          <= ip_out_tmp[31: 0];
                        rnd_cnt    <= 4'd0;
                        phase      <= 4'd1;
                    end
                end
                4'd1: begin
                    // One Feistel round
                    sk_idx_tmp = enc_reg ? rnd_cnt : (4'd15 - rnd_cnt);
                    f_out_tmp  = f_func(R, subkeys[sk_idx_tmp]);
                    new_R_tmp  = L ^ f_out_tmp;
                    L          <= R;
                    R          <= new_R_tmp;
                    if (rnd_cnt == 4'd15) begin
                        phase <= 4'd2;
                    end else begin
                        rnd_cnt <= rnd_cnt + 4'd1;
                    end
                end
                4'd2: begin
                    // Swap L/R then apply IP^-1
                    result <= ip_inv({R, L});
                    done   <= 1'b1;
                    phase  <= 4'd0;
                end
            endcase
        end
    end
endmodule
`default_nettype wire
